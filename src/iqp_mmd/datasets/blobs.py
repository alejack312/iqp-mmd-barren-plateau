"""Spin blob dataset generation."""

from pathlib import Path
from dataclasses import dataclass

import numpy as np


@dataclass
class BlobsDataset:
    """Container for spin blobs dataset."""

    train_samples: np.ndarray
    test_samples: np.ndarray
    train_labels: np.ndarray
    test_labels: np.ndarray
    peak_spins: np.ndarray
    settings: dict


def generate_spin_blobs(
    n_spins: int = 16,
    n_blobs: int = 20,
    num_train: int = 5000,
    num_test: int = 50000,
    peak_weight: int = 8,
    noise_prob: float = 0.05,
    seed: int = 61,
    output_dir: str | Path = None,
) -> BlobsDataset:
    """Generate a synthetic classification dataset of noisy spin blobs.

    Each blob is centered on a random low-weight bitstring "peak".
    Samples are generated by flipping bits near the peak with given noise.

    Args:
        n_spins: Number of spins (binary features).
        n_blobs: Number of distinct blob classes.
        num_train: Training samples.
        num_test: Test samples.
        peak_weight: Average Hamming weight of peak configurations.
        noise_prob: Probability of bit flip per spin.
        seed: Random seed.
        output_dir: If provided, save CSVs here.

    Returns:
        BlobsDataset with train/test data and labels.
    """
    from qml_benchmarks.data.spin_blobs import RandomSpinBlobs

    np.random.seed(seed)

    p_flip = 1 / n_spins * peak_weight
    peak_spins = []
    while len(peak_spins) < n_blobs:
        bitstring = list((-1) ** np.random.binomial([1] * n_spins, [p_flip] * n_spins))
        if bitstring not in peak_spins:
            peak_spins.append(bitstring)

    def distance_sampler():
        return np.random.binomial(n_spins, noise_prob)

    sampler = RandomSpinBlobs(N=n_spins, num_blobs=n_blobs, distance_sampler=distance_sampler, peak_spins=peak_spins)
    X, y = sampler.sample(num_train + num_test, return_labels=True)
    X = -X

    dataset = BlobsDataset(
        train_samples=X[:num_train],
        test_samples=X[num_train:],
        train_labels=y[:num_train],
        test_labels=y[num_train:],
        peak_spins=np.array(peak_spins),
        settings={
            "n_spins": n_spins,
            "n_blobs": n_blobs,
            "num_train": num_train,
            "num_test": num_test,
            "peak_weight": peak_weight,
            "noise_prob": noise_prob,
        },
    )

    if output_dir:
        output_dir = Path(output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)
        np.savetxt(output_dir / "train.csv", dataset.train_samples, delimiter=",", fmt="%d")
        np.savetxt(output_dir / "test.csv", dataset.test_samples, delimiter=",", fmt="%d")
        np.savetxt(output_dir / "labels_train.csv", dataset.train_labels, delimiter=",", fmt="%d")
        np.savetxt(output_dir / "labels_test.csv", dataset.test_labels, delimiter=",", fmt="%d")
        np.savetxt(output_dir / "peak_spins.csv", dataset.peak_spins, delimiter=",", fmt="%d")

    return dataset
